public class Arithmetics {
    public static void main(String[] args) {
        double amountKWD = 86;
double amountUSA = 200;
double amountEUR = 100;
        System.out.println(amountKWD + " KWD" + " = " + amountKWD * 3.25 +" USD");
        System.out.println(amountUSA + " USD" + " = " + amountUSA / 3.25 +" KWD");
        System.out.println(amountEUR + " EUR" + " = " + amountEUR * 0.33 +" KWD");
    }
}
